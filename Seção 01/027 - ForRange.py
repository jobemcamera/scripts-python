# For / Range

# numeros de 0 a 10
numeros = range(10)
for numero in numeros:
  print(numero) 
  
# numeros de 5 a 10
numeros = range(5, 10)
for numero in numeros:
  print(numero) 
  
# numeros de 0 a 10 pulando de 2
numeros = range(5, 10, 2)
for numero in numeros:
  print(numero) 

# numeros de 10 a 2 pulando de -2
numeros = range(10, 0, -2)
for numero in numeros:
  print(numero) 

